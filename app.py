print(f"R² Score : {r2:.3f}")

if r2 >= 0.90:
    print("\nThe model explains most of the variation in the temperature data.")
elif r2 >= 0.70:
    print("\nThe model provides a reasonably good fit to the data.")
else:
    print("\nThe model has limited predictive performance and may benefit from additional feature engineering or parameter tuning.")

print("\nLearning Curve Interpretation:")

gap = validation_mean[-1] - train_mean[-1]

if gap > 2:
    print("The validation error remains noticeably higher than the training error, suggesting possible overfitting.")
elif abs(gap) < 0.5:
    print("Training and validation errors are similar, suggesting good generalisation.")
else:
    print("The model may benefit from additional training data or hyperparameter tuning.")
