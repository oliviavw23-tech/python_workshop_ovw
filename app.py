"""# Part 2. Feature Engineering and XGBoost Regression Model"""

# ============================================================
# PART 2
# Feature Engineering and XGBoost Regression Model
# ============================================================

# ------------------------------------------------------------
# Import machine learning libraries
# ------------------------------------------------------------

from sklearn.model_selection import TimeSeriesSplit
from xgboost import XGBRegressor

# ------------------------------------------------------------
# Feature Engineering
# ------------------------------------------------------------

# Create lag features (previous 7 days)

for lag in range(1, 8):
    df[f"lag_{lag}"] = df["meantemp"].shift(lag)

# ------------------------------------------------------------
# Create time-based features
# ------------------------------------------------------------

df["year"] = df["date"].dt.year
df["month"] = df["date"].dt.month
df["day"] = df["date"].dt.day
df["day_of_week"] = df["date"].dt.dayofweek
df["day_of_year"] = df["date"].dt.dayofyear
df["week_of_year"] = df["date"].dt.isocalendar().week.astype(int)

# Optional weekend indicator
df["is_weekend"] = df["day_of_week"].isin([5, 6]).astype(int)

# ------------------------------------------------------------
# Remove rows containing NaN values introduced by lag features
# ------------------------------------------------------------

df = df.dropna().reset_index(drop=True)

print("Dataset shape after creating lag features:")
print(df.shape)

display(df.head())

# ------------------------------------------------------------
# Define Feature Matrix (X) and Target Variable (y)
# ------------------------------------------------------------

feature_columns = [
    "humidity",
    "wind_speed",
    "meanpressure",
    "lag_1",
    "lag_2",
    "lag_3",
    "lag_4",
    "lag_5",
    "lag_6",
    "lag_7",
    "month",
    "day",
    "day_of_week",
    "day_of_year",
    "week_of_year",
    "is_weekend"
]

X = df[feature_columns]

y = df["meantemp"]

print("Feature matrix shape:", X.shape)
print("Target vector shape:", y.shape)

# ------------------------------------------------------------
# Chronological Train/Test Split
# ------------------------------------------------------------

train_size = int(len(df) * 0.8)

X_train = X.iloc[:train_size]
X_test = X.iloc[train_size:]

y_train = y.iloc[:train_size]
y_test = y.iloc[train_size:]

print("\nTraining samples:", len(X_train))
print("Testing samples :", len(X_test))

# ------------------------------------------------------------
# TimeSeriesSplit for Cross-Validation
# ------------------------------------------------------------

tscv = TimeSeriesSplit(n_splits=5)

print("\nTimeSeriesSplit Folds")

for fold, (train_index, val_index) in enumerate(tscv.split(X_train), start=1):

    print(f"\nFold {fold}")
    print(f"Training observations : {len(train_index)}")
    print(f"Validation observations: {len(val_index)}")

# ------------------------------------------------------------
# Build the XGBoost Regressor
# ------------------------------------------------------------

model = XGBRegressor(
    objective="reg:squarederror",
    n_estimators=300,
    learning_rate=0.05,
    max_depth=4,
    subsample=0.8,
    colsample_bytree=0.8,
    random_state=42
)

# ------------------------------------------------------------
# Train the Model
# ------------------------------------------------------------

model.fit(X_train, y_train)

print("\nModel training completed successfully.")

"""# Part 3. Model Evaluation and Visualisation"""

# ============================================================
# PART 3
# Model Evaluation and Visualisation
# ============================================================

# ------------------------------------------------------------
# Import evaluation libraries
# ------------------------------------------------------------

from sklearn.metrics import (
    mean_absolute_error,
    mean_squared_error,
    r2_score
)

from sklearn.model_selection import learning_curve

# ------------------------------------------------------------
# Make Predictions
# ------------------------------------------------------------

y_pred = model.predict(X_test)

# ------------------------------------------------------------
# Regression Metrics
# ------------------------------------------------------------

mae = mean_absolute_error(y_test, y_pred)
mse = mean_squared_error(y_test, y_pred)
rmse = np.sqrt(mse)
r2 = r2_score(y_test, y_pred)

print("=" * 50)
print("Regression Performance")
print("=" * 50)
print(f"MAE : {mae:.3f}")
print(f"MSE : {mse:.3f}")
print(f"RMSE: {rmse:.3f}")
print(f"R²  : {r2:.3f}")

# ------------------------------------------------------------
# Actual vs Predicted
# ------------------------------------------------------------

plt.figure(figsize=(15,6))

plt.plot(
    y_test.values,
    label="Actual",
    linewidth=2
)

plt.plot(
    y_pred,
    label="Predicted",
    linewidth=2
)

plt.title("Actual vs Predicted Mean Temperature")
plt.xlabel("Observation")
plt.ylabel("Mean Temperature")
plt.legend()
plt.grid(True)

plt.show()

# ------------------------------------------------------------
# Residual Plot
# ------------------------------------------------------------

residuals = y_test - y_pred

plt.figure(figsize=(8,6))

plt.scatter(
    y_pred,
    residuals,
    alpha=0.7
)

plt.axhline(
    y=0,
    color="red",
    linestyle="--"
)

plt.xlabel("Predicted Values")
plt.ylabel("Residuals")
plt.title("Residual Plot")

plt.show()

# ------------------------------------------------------------
# Feature Importance
# ------------------------------------------------------------

importance = pd.DataFrame({

    "Feature": feature_columns,
    "Importance": model.feature_importances_

})

importance = importance.sort_values(
    by="Importance",
    ascending=False
)

display(importance)

plt.figure(figsize=(10,6))

sns.barplot(
    data=importance,
    x="Importance",
    y="Feature"
)

plt.title("Feature Importance")

plt.show()

# ------------------------------------------------------------
# Learning Curve using TimeSeriesSplit
# ------------------------------------------------------------

train_sizes, train_scores, validation_scores = learning_curve(

    estimator=model,

    X=X_train,

    y=y_train,

    cv=tscv,

    scoring="neg_mean_squared_error",

    train_sizes=np.linspace(0.2, 1.0, 5),

    n_jobs=-1

)

train_scores = np.sqrt(-train_scores)
validation_scores = np.sqrt(-validation_scores)

train_mean = train_scores.mean(axis=1)
validation_mean = validation_scores.mean(axis=1)

plt.figure(figsize=(10,6))

plt.plot(
    train_sizes,
    train_mean,
    marker="o",
    label="Training RMSE"
)

plt.plot(
    train_sizes,
    validation_mean,
    marker="o",
    label="Validation RMSE"
)

plt.title("Learning Curve")
plt.xlabel("Training Examples")
plt.ylabel("RMSE")
plt.legend()
plt.grid(True)

plt.show()

# ------------------------------------------------------------
# Interpretation
